package scheduling.dvms;

import java.io.Serializable;
import java.net.UnknownHostException;
import org.simgrid.msg.Host;

import dvms.clientserver.Server;

//Represents a general server
public class ServerForSG extends Server implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	// Just a simple way to override the constructors and bypass the fact that real hostname is retrieved in Server.
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//Constructors
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	public ServerForSG(){
		super(null,-1);
	}
	
	public ServerForSG(String host, int port) {
		super(host,port);
	}
	
	public ServerForSG(int port) throws UnknownHostException{
		this(Host.currentHost().getName(), port);
	}

	public ServerForSG(ServerForSG server) {
		super(server);
	}
	
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//Accessors
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

		
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//Other methods
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	
}
